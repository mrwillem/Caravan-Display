#define TANKROWS 100

uint8_t prevlevel[2];

void watersensor_setup()
{
	// Port initialization for the water sensor goes here
	// 0 % -> No Signal
	// 25% -> PB15
	// 50% -> PB14
	// 75% -> PB13
	// 100%-> PB12
	// And the last sensor is the Wastewater sensor
	// Waste -> PA8
	GPIO_InitTypeDef  GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12 | GPIO_Pin_13 | GPIO_Pin_14 | GPIO_Pin_15;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOB, &GPIO_InitStructure);

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOA, &GPIO_InitStructure);

}

/* This function returns 1 if the wastewater sensor is conductive */
uint8_t get_wastelevel()
{
	if (GPIO_ReadInputDataBit(GPIOA, GPIO_Pin_8) == 1)
	{
		return(1);
	}
	else
	{
		return(0);
	}
}

/* This functions returns either 100, 75, 50 or 25 depending on which waterlevel is detected */
uint8_t get_waterlevel()
{
	if (GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_12) == 1)
	{
		return(100);
	}
	else
	{
		if(GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_13) == 1)
		{
			return(75);
		}
		else
		{
			if(GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_14) == 1)
			{
				return(50);
			}
			else
			{
				if(GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_15) == 1)
				{
					return(25);
				}
				else
				{
					return(0);
				}
			}
		}
	}
}

void update_water_display()
{
	uint16_t waterlevel;
	uint8_t wastelevel;
	waterlevel = (uint16_t) (1.5*(get_waterlevel()));
	if (waterlevel != prevlevel[0])
	{
		LCD_DrawWaterTank(0x70, 0x70, (150-waterlevel), waterlevel , GL_Blue, GL_White);
	}
	wastelevel = get_wastelevel();
	if (wastelevel != prevlevel[1])
	{
		prevlevel[1]=wastelevel;
		if(wastelevel == 0)
		{
			LCD_DrawWasteTank(0x70, 0x0, GL_White, GL_White);
		}
		else
		{
			LCD_DrawWasteTank(0x70, 0x0, GL_Red, GL_White);
		}
	}
}
